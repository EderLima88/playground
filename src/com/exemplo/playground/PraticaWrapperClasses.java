package com.exemplo.playground;

public class PraticaWrapperClasses {
    public static void main(String[] args) {
        int letra = 'A';
        int letraM = letra + 32;
        char letraMaic = (char) letraM;

        System.out.println("Em ASC: " + letra);
        System.out.println(letraM);
        System.out.println(letraMaic);
    }
}
